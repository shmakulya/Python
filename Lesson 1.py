# Задание 5 Запросите у пользователя значения выручки и издержек фирмы. Определите, с каким
# финансовым результатом работает фирма (прибыль — выручка больше издержек, или убыток — издержки
# больше выручки). Выведите соответствующее сообщение. Если фирма отработала с прибылью, вычислите
# рентабельность выручки (соотношение прибыли к выручке). Далее запросите численность сотрудников
# фирмы и определите прибыль фирмы в расчете на одного сотрудника.

revenue = int(input("Введите значение выручки: " "$"))
cost = int(input("Введите значение издержек: " "$"))
if revenue > cost:
    print("Прибыль")
    profitability = int(100 / revenue * (revenue - cost))
    print("Рентабельность:", profitability, "%")
    employees = int(input("Введите количество сотрудников: "))
    revenue_employees = int((revenue - cost) / employees)
    print("Прибыль фирмы на 1 сотрудника: ",revenue_employees, "$")
else:
    print("Убыток")


