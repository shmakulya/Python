# Задание 2   Пользователь вводит время в секундах. Переведите время в часы, минуты и секунды
# и выведите в формате чч:мм:сс. Используйте форматирование строк.

sec = int(input("Введите время в секундах: "))
hour = sec // 3600
min = (sec - 3600*hour) // 60
sek = sec - min * 60 - hour * 3600
print(f'Время: {hour:02}:{min:02}:{sek:02}')


